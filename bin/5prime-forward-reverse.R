inserts <- scan('../results/5primeinserts.txt', sep="\n")
forrev = c(sum(inserts[1:3]) * 100 / sum(inserts), sum(inserts[4:6]) * 100 / sum(inserts))
names(forrev) <- c('F', 'R')
insperc = inserts*100/sum(inserts)
names(insperc) <- c('1','2','3','4','5','6')
pdf('../figures/5prime-frames.pdf')
m <- rbind(c(0, 0.35, 0, 1), c(0.35, 1, 0, 1))
temp <- split.screen(m)
mar.default <- c(5,4,4,2) + 0.1
par(mar = mar.default + c(0, 1, 0, 0))
screen(1)
barplot(forrev, xlab = 'Strand', ylab = '% Insertions')
abline(50,0, col='red', lwd=3)
screen(2)
barplot(insperc, xlab = 'Frame', ylab = '% Insertions')
abline(100/6,0, col='red', lwd=3)
title( "Percentage of insertions in different frames in 5\' ends of essential genes", outer = TRUE, line=-1.5)
close.screen(all.screens = TRUE)
dev.off()